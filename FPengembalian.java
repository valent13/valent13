    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        try{
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
            conn=DriverManager.getConnection("jdbc:odbc:PERPUS");
            if(txtNoPinjam.getText().isEmpty() || tabModel.getRowCount()==0 ){
                JOptionPane.showMessageDialog(this,"Silahkan Input No Pinjam");
            } else {
                // Menyimpan ke Tabel Pinjam
                String sql="insert into Pengembalian values(?,?,?,?,?,?,?)";
                PreparedStatement st=conn.prepareStatement(sql);
                st.setString(1,txtNoPengembalian.getText());
                st.setString(2,txtNoPinjam.getText());
                st.setString(3,txtDenda.getText());
                st.setString(4,txtTglPinjam.getText());
                st.setString(5,txtTglKembali.getText());
                st.setString(6,txtTglDikembalikan.getText());
                st.setString(7,txtTerlambat.getText());
                int rs= st.executeUpdate();

                String KodeBuku; int rs2=0;

                int jum=tabModel.getRowCount();
                for(int i=0;i<jum;i++){
                    // Menyimpan Ke detail Pinjam
                    String sql2="insert into DetailPengembalian values(?,?,?)";
                    PreparedStatement st2=conn.prepareStatement(sql2);
                    KodeBuku = tabModel.getValueAt(i, 1).toString();
                    st2.setString(1,txtNoPengembalian.getText());
                    st2.setString(2,KodeBuku);
                    st2.setString(3,"1");
                    rs2= st2.executeUpdate();
                    // Merubah status Buku
                    String sql3="update Buku set Status=? where KodeBuku=?";
                    PreparedStatement st3=conn.prepareStatement(sql3);
                    st3.setString(1,"Ada");
                    st3.setString(2, KodeBuku);
                    st3.executeUpdate();
                }
                if((rs>0) && (rs2>0)){
                    JOptionPane.showMessageDialog(this,"Input Berhasil");
                    AutoNumber();
                    resetForm();
                    hapusIsiJTable();
                } else
                JOptionPane.showMessageDialog(this,"Input Gagal");
                conn.close();
            }

        } // akhir try
        catch(Exception e){
            System.out.println("Exception " +e );
        }
    }//GEN-LAST:event_BSimpanActionPerformed

