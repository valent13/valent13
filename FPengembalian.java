    public void AutoNumber() {
        try {
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
            conn=DriverManager.getConnection("jdbc:odbc:PERPUS");
            String sql="Select * from Pengembalian ORDER BY NoPengembalian DESC";
            Statement stt = conn.createStatement();
            ResultSet rs = stt.executeQuery(sql);
            if (rs.next()) {
                String NoPengembalian = rs.getString("NoPengembalian").substring(1);
                String AN = "" + (Integer.parseInt(NoPengembalian) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}
                txtNoPengembalian.setText("K" + Nol + AN);//sesuaikan dengan variable namenya
            } else {
                txtNoPengembalian.setText("K0001");//sesuaikan dengan variable namenya
            }
            rs.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();//penanganan masalah
        }
    }
      
