    private void getPinjam(){
        try {
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
            conn = DriverManager.getConnection("jdbc:odbc:PERPUS");
            String sql = "SELECT *,Nama FROM Pinjam INNER JOIN Anggota on Pinjam.NoAnggota = Anggota.NoAnggota Where NoPinjam=?";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, txtNoPinjam.getText());
            ResultSet rs = st.executeQuery();
            if (rs.next()) {
                Date tglPinjam = rs.getDate("tglPinjam");
                SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
                String formatTglPinjam = formatter.format(tglPinjam);
                
                Date tglKembali = rs.getDate("tglKembali");
                SimpleDateFormat simpleFormat = new SimpleDateFormat("dd/MM/yyyy");
                String formatTglKembali = simpleFormat.format(tglKembali);
                
                String Nama = rs.getString("Nama");
                txtNama.setText(Nama);
                txtNoAnggota.setText(rs.getString("NoAnggota"));
                txtTglPinjam.setText(formatTglPinjam);
                txtTglKembali.setText(formatTglKembali);
                
                
                Date tgl_sekarang = new Date();
                SimpleDateFormat format=new SimpleDateFormat("dd/MM/yyyy");
                String formatTglDikembalikan=format.format(tgl_sekarang);
                txtTglDikembalikan.setText(formatTglDikembalikan);
                
                SimpleDateFormat myFormat = new SimpleDateFormat("dd/MM/yyyy");
                String tgl = formatTglKembali;
                String tgl2 = formatTglDikembalikan;
                
                int denda = 0;
                try {
                    Date date1 = myFormat.parse(tgl);
                    Date date2 = myFormat.parse(tgl2);
                    long diff = date2.getTime() - date1.getTime();
                    denda = (int) (TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS));
                    if(denda >= 0){
                        txtTerlambat.setText(String.valueOf(denda));
                    }else{
                        denda = 0;
                        txtTerlambat.setText("0");
                    }
                } catch (ParseException e) {
                    e.printStackTrace();
                }
                int hasil = 500 * denda;
                txtDenda.setText(String.valueOf(hasil));
            } else {
                JOptionPane.showMessageDialog(this, "No Pinjam Tidak ada!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                txtNoPinjam.setText("");
            }
        }
        catch (Exception e) {
            System.out.println("Koneksi Gagal " + e.getMessage());
        }
    }
    
